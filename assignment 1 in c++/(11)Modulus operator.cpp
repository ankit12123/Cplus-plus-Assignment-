//Write a program to find the remainder of two integers using the modulus operator.
#include<iostream>
using namespace std;
int main(){
        int a,b,rem;

         cout<<"Enter the Two Numbers :";
         cin>>a>>b;

        // using Modulus operator.
         rem=a%b;

         cout<<"The Remainder Of two Number using Modulus operator is: "<<rem;
}
